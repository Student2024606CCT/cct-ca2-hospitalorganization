
package cctcollege.util;

/**
 * 
 * QuickSort
 * 
 * This interface defines a contract for classes that implement
 * the QuickSort algorithm. Any class that implements this interface
 * must provide its own version of the quickSort() method to perform
 * in-place sorting of its elements.
 * 
 * @author asafeds
 */
public interface QuickSort {

    /**
     * Sorts the current data structure using the QuickSort algorithm.
     */
    public void quickSort();
}
